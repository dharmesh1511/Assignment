# Import math module
import math

# Using sqrt() to find square root
num = 16
print("Square root of", num, "is:", math.sqrt(num))

# Using ceil() to round up
value = 6.15
print("Ceiling of", value, "is:", math.ceil(value))

# Using floor() to round down
value = 89.6167
print("Floor of", value, "is:", math.floor(value))
