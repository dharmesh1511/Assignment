#include <iostream>
using namespace std;

int main() {
    int num1 = 10;
    float num2 = 5.5;
    char grade = 'A';
    bool isPassed = true;

    // Constant
    const double PI = 3.14;

    // Performing operations
    float result = num1 + num2;

    cout << "Integer num1: " << num1 << endl;
    cout << "Float num2: " << num2 << endl;
    cout << "Character grade: " << grade << endl;
    cout << "Boolean isPassed: " << isPassed << endl;
    cout << "Constant PI: " << PI << endl;
    cout << "Result of num1 + num2: " << result << endl;

    return 0;
}
